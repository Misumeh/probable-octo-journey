function applyFilters() {
    const ver = document.getElementById('verFilter').value;
    const res = document.getElementById('resFilter').value;
    const col = document.getElementById('colorFilter').value;
    const search = document.getElementById('searchInput').value.toLowerCase();

    const filtered = allPacks.filter(p => {
        const matchesVer = ver === 'all' || p.links[ver];
        const matchesRes = res === 'all' || p.tags.res === res;
        const matchesCol = col === 'all' || p.tags.color === col;
        const matchesSearch = p.name.toLowerCase().includes(search) || 
                              p.tags.origin.toLowerCase().includes(search);
        
        return matchesVer && matchesRes && matchesCol && matchesSearch;
    });

    renderGrid(filtered);
}

function renderGrid(packs) {
    const grid = document.getElementById('packGrid');
    grid.innerHTML = '';

    packs.forEach(pack => {
        const card = document.createElement('div');
        card.className = "group cursor-pointer border border-white/5 p-4 hover:border-purple-600 transition-all bg-zinc-950/20";
        card.onclick = () => openPack(pack);
        
card.innerHTML = `
    <div class="aspect-video overflow-hidden bg-zinc-900 mb-6">
        <img src="${pack.thumb}" class="w-full h-full object-cover grayscale-0 group-hover:grayscale transition-all duration-700">
    </div>
    <div class="flex flex-wrap gap-2 mb-4">
        <div class="tag-box">${pack.tags.res}</div>
        <div class="tag-box">${pack.tags.color}</div>
        
        ${pack.links.nsfw ? `<div class="tag-box border-red-500 text-red-500">NSFW AVAILABLE</div>` : ''}
    </div>
    <h4 class="text-2xl font-black uppercase tracking-tighter group-hover:text-purple-400 transition-all italic">${pack.name}</h4>
    <p class="text-[10px] text-zinc-600 font-bold uppercase tracking-widest mt-1">${pack.tags.origin}</p>
`;
        grid.appendChild(card);
    });
}

function openPack(pack) {
    const modal = document.getElementById('detailModal');
    const media = document.getElementById('modalMedia');
    
    media.innerHTML = pack.yt 
        ? `<iframe class="w-full h-full aspect-video" src="https://www.youtube.com/embed/${pack.yt}?autoplay=1&mute=1" frameborder="0" allowfullscreen></iframe>`
        : `<img src="${pack.thumb}" class="w-full h-full object-cover aspect-video">`;

    document.getElementById('modalName').innerText = pack.name;
    document.getElementById('modalDesc').innerText = pack.desc;
    
    const tags = document.getElementById('modalTags');
    tags.innerHTML = `<div class="tag-box text-purple-400 border-purple-400/20">${pack.tags.origin}</div><div class="tag-box">${pack.tags.res}</div>`;

    const links = document.getElementById('modalLinks');
    links.innerHTML = '';
    const labels = { j189: "Java 1.8.9", j121: "Java 1.21", bedrock: "Bedrock Edition", nsfw: "🔞 Adult Version" };

    Object.entries(pack.links).forEach(([key, url]) => {
        if(url) {
            const btn = document.createElement('button');
            btn.className = "w-full text-left py-4 px-6 border border-white/5 hover:bg-white hover:text-black transition-all text-[10px] font-black uppercase tracking-widest flex justify-between";
            btn.innerHTML = `${labels[key]} <span>[DOWNLOAD]</span>`;
            
            // --- FIX START ---
            btn.onclick = () => {
                if (key === 'nsfw') {
                    // This triggers the 18+ verification popup
                    document.getElementById('confirmNsfw').href = url;
                    document.getElementById('nsfwModal').classList.remove('hidden');
                } else {
                    // This triggers the Patreon/Ad popup
                    document.getElementById('adLink').href = url;
                    document.getElementById('patreonModal').classList.remove('hidden');
                }
            };
            // --- FIX END ---
            
            links.appendChild(btn);
        }
    });

    modal.classList.remove('hidden');
    document.body.style.overflow = 'hidden';
}

